    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser filechooser = new JFileChooser();
        if (filechooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            arquivo = filechooser.getSelectedFile(); //minha variavel arquivo vira o arquivo escolhido pela pessoa
            jTextField1.setText(arquivo.getAbsolutePath()); //coloco o caminho do arquivo escrito
            jButton2.setEnabled(true);//se o usuario selecionar um  arquivo, o botao carregar é habilitado
        } else {
            JOptionPane.showMessageDialog(this, "Você cancelou a operação.", "Operação cancelada pelo usuário.", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


