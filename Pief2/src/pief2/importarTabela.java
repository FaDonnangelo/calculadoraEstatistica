    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            FileReader fileReader = new FileReader(arquivo);
            BufferedReader buffer = new BufferedReader(fileReader);
            String linha;
            StringTokenizer tokens;
            DefaultTableModel model = (DefaultTableModel) caller.tabela.getModel();
            linha = buffer.readLine();

            while (linha != null) {
                tokens = new StringTokenizer(linha, jTextField2.getText());
                Object row[] = new Object[tokens.countTokens()];
                row[0] = new Double(tokens.nextToken());
                row[1] = new Integer(tokens.nextToken());
                model.addRow(row);
                linha = buffer.readLine();
            }
            caller.tabela.setModel(model);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "ERRO!", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButton2ActionPerformed

